    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String year = jComboBox2.getSelectedItem().toString();
        String sem = jComboBox1.getSelectedItem().toString();
        String courseid = jTextField1.getText();

        try {
            ResultSet rs = DbHandler.getData("Select subname,Amount from grup_project.elective_subjects where courseid='" + courseid + "' and semno='" + sem + "' and Year= '" + year + "'");
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            int row = dt.getRowCount();
            Vector data = new Vector();

            for (int i = 0; i < row; i++) {
                dt.removeRow(i);
            }

            while (rs.next()) {

                Object pq[] = {rs.getString(1), rs.getString(2)};
                dt.addRow(pq);
            }
            //  Get row data
            while (rs.next()) {
                Vector row1 = new Vector(row);
                for (int i = 1; i <= row; i++) {
                    row1.addElement(rs.getObject(i));
                }
                data.addElement(row1);
            }
            rs.close();

        } catch (SQLException ex) {
            Logger.getLogger(SubAmountGen.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SubAmountGen.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ArrayIndexOutOfBoundsException ex) {
            Logger.getLogger(SubAmountGen.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

