    public Double getTotalPayment() {
        int rowsCount = jTable1.getSelectedRowCount();
        Double sum = 0.00;
        for (int i = 0; i < rowsCount; i++) {
            sum = sum + Double.parseDouble(jTable1.getValueAt(i, 1).toString());
        }
        return sum;
    }

