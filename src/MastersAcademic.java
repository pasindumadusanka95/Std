    private void jButtonNextregActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonNextregActionPerformed
        String subject1 = sub1.getText();
        String subject2 = sub2.getText();
        String subject3 = sub3.getText();
        String subject4 = sub4.getText();
        String sub1grade = subg1.getText();
        String sub2grade = subg2.getText();
        String sub3grade = subg3.getText();
        String sub4grade = subg4.getText();
        String irank = rank.getText();
        String zscore1 = zscore.getText();
        String studentID = stdid.getText();

        try {

            if (Validation_Material() == true) {
                DbHandler.setData("INSERT INTO grup_project.alresults(rank,subject1,subject2,subject3,subject4,s1grade,s2grade,s3grade,s4grade,zscore,sid) values('" + irank + "','" + subject1 + "','" + subject2 + "','" + subject3 + "','" + subject4 + "','" + sub1grade + "','" + sub2grade + "','" + sub3grade + "','" + sub4grade + "','" + zscore1 + "','" + studentID + "')");

                JOptionPane.showMessageDialog(null, "Values successfuly Inserted");

                CourseSelection rfg2 = new CourseSelection();
                rfg2.setVisible(true);
                rfg2.pack();
                rfg2.setLocationRelativeTo(null);
                rfg2.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }

        sub1.setText(null);
        sub2.setText(null);
        sub3.setText(null);
        sub4.setText(null);
        stdid.setText(null);
        subg2.setText(null);
        subg3.setText(null);
        subg4.setText(null);
        zscore.setText(null);
        rank.setText(null);

        
    }//GEN-LAST:event_jButtonNextregActionPerformed

